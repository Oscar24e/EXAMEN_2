                            <h2 class="entry-title">
                                <a title="Conceptos de Infrastructure as a Service (IAAS), Platform as a Service (PAAS), Software as a Service (SAAS).">
                                    INFRAESTRUCTURAS </a>
                            </h2>
                            <div class="post-content">
                                <p>
                                    <center>
                                        <h2>IAAS</h2>
                                    </center>
                                </p>
                                <p>Infraestructure as a Service que en español es infraestructura como servicio</p>
                                <p>Es la base del servicio y contiene los bloques fundamentales para la TI en la nube, pues proporciona los servicios y estructuras necesarios sobre los cuales se construyen los otros servicios.</p>
                                <p>
                                    <center>
                                        <h4>ARQUITECTURA </h4>
                                    </center>
                                </p>
                                <p>Infraestructura como un servicio</p>
                                <p>Este servicio proporciona una infraestructura informática que se aprovisiona y administra a través de internet. Un proveedor de IaaS gestiona la parte física de la infraestructura (como servidores o espacio de almacenamiento de datos) en un centro de datos, pero permite a los clientes personalizar completamente esos recursos virtualizados para satisfacer sus necesidades específicas. </p>
                                <p>Con IaaS, el cliente puede comprar, instalar, configurar y administrar cualquier software que necesite usar, incluidos elementos como sistemas operativos, middleware, aplicaciones, análisis de negocios y herramientas de desarrollo.</p>


                                <p>
                                    <center>
                                        <h2>PAAS</h2>
                                    </center>
                                </p>
                                <p>Platform as a Service que en español es plataforma como servicio </p>
                                <p>Ofrece plataformas como servicios. En estas plataformas se pueden lanzar aplicaciones como bases de datos, middleware, herramientas de desarrollo, servicios de inteligencia empresarial, etc.</p>
                                <p>
                                    <center>
                                        <h4>ARQUITECTURA</h4>
                                    </center>
                                </p>
                                <p>Plataforma como servicio</p>
                                <p>A través de este servicio en la nube, los desarrolladores de software tienen acceso a herramientas basadas en la nube como API, software de puerta de enlace o portales web. Servicios como Lightning de Salesforce, Google App Engine y Elastic Beanstalk de AWS son soluciones populares. Bajo estas soluciones, el Proveedor de la solución PaaS administra la infraestructura y también la capa del sistema operativo.</p>

                                <p>
                                    <center>
                                        <h2>SAAS</h2>
                                    </center>
                                </p>
                                <p>Software as a Service, en español software como servicio.</p>
                                <p>Uso de una aplicación sin adquirir la misma, a través del pago de una cuota a un proveedor. Si lo vinculamos al cloud computing</p>
                                <p>
                                    <center>
                                        <h4>ARQUITECTURA </h4>
                                    </center>
                                </p>
                                <p>Software como servicio</p>
                                <p>Este tipo de servicio brinda a los usuarios acceso a software a través de internet. Las aplicaciones SaaS, accesibles a través de computadoras y dispositivos móviles con acceso a internet, permiten a los trabajadores colaborar en proyectos, descargar archivos importantes y trabajar directamente en programas informáticos especializados. </p>
                                <p>Las empresas más pequeñas pueden acceder fácilmente a aplicaciones sofisticadas de nivel empresarial, como los programas de gestión de relaciones con el cliente (CRM) o planificación de recursos empresariales (ERP), proporcionándoles herramientas que les permiten hacer crecer sus negocios de forma más eficaz que nunca</p>


