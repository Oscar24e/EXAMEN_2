                            <h2 class="entry-title">
                                <a title="arquitectura  Cliente/Servidor">
                                    Arquitectura Cliente/Servidor </a>
                            </h2>
                            <div class="post-content">
                                <p>
                                    <center>
                                        <h4>SERVICIOS</h4>
                                    </center>
                                </p>
                                <p>Este es una red de comunicaciones en la cual los clientes estan conectados a un servidor, en el que se centralizan
                                    diversos recursos y aplicaciones con que se cuentan, y que los pone a disposicion de los clientes cada vez que estos
                                    son solicitados. </p>

                                <p>
                                    <center>
                                        <h4>RECURSOS COMPARTIDOS </h4>
                                    </center>
                                </p>
                                <p>Esta implica compartir dispositivos de una computadora que actua como servidor y las otras computadoras como
                                    clientes compartiendo desde la impresora hasta otros archivos </p>

                                <p>
                                    <center>
                                        <h4>PROTOCOLOS ASIMETRICOS </h4>
                                    </center>
                                </p>
                                <p>Hay una relacion muchos a uno entre los clientes y un servicio.</p>
                                <p>Los clientes siempre incian un dialogo mediante la solicitud de un servicio, mientras que el servidor espera
                                    pasivamente por dichas solicitudes.</p>

                                <p>
                                    <center>
                                        <h4>INDEPENDENCIA DE LA UBICACIÓN </h4>
                                    </center>
                                </p>
                                <p>Su representación típica es un centro de trabajo (PC), en donde el usuario dispone de sus propias aplicaciones
                                    de oficina y sus propias bases de datos, sin dependencia directa del sistema central de información de la organización.</p>

                                <p>
                                    <center>
                                        <h4>COMPATIBILIDAD DE CLIENTES Y SERVIDORES</h4>
                                    </center>
                                </p>
                                <p>El Cliente y el Servidor pueden actuar como una sola entidad y también pueden actuar como entidades separadas,
                                    realizando actividades o tareas independientes</p>

                                <p>
                                    <center>
                                        <h4>COMUNICACIÓN DE MENSAJES </h4>
                                    </center>
                                </p>
                                <p>Al receptor de la solicitud enviada por el cliente se le conoce como servidor y el remitente de una solicitud
                                    es conocido como cliente. </p>
                                <p>Tras la recepción de una solicitud, la procesa y luego envía la respuesta al cliente.</p>

                                <p>
                                    <center>
                                        <h4>ENCAPSULACION DE SERVICIOS </h4>
                                    </center>
                                </p>
                                <p>Por lo general, acepta las conexiones de un gran número de clientes (en ciertos casos el número máximo de peticiones puede estar
                                    limitado).</p>

                                <p>
                                    <center>
                                        <h4>ESCALABILIDAD</h4>
                                    </center>
                                </p>
                                <p>Cada plataforma puede ser escalable independientemente. Los cambios realizados en las plataformas de los Clientes o
                                    de los Servidores, ya sean por actualización o por reemplazo tecnológico, se realizan de una manera transparente
                                    para el usuario final.</p>

