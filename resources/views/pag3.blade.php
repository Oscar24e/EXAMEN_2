                                <p>
                                    <center>
                                        <h4>SERVIDOR DE LA TELEFONIA </h4>
                                    </center>
                                </p>
                                <p>Este tiene relacion con funciones unicamente relacionadas con la telefonia, controlando tambien la red o el internet</p>

                                <p>
                                    <center>
                                        <h4>SERVIDOR PROXY </h4>
                                    </center>
                                </p>
                                <p>Realiza un tipo de funciones a nombre de otros clientes en la red para asi aumentar el funcionamiento de ciertas
                                    operaciones. </p>

                                <p>
                                    <center>
                                        <h4>SERVIDOR DEL ACCESO REMOTO (RAS)</h4>
                                    </center>
                                </p>
                                <p>Este servidor controla las lineas de modem de los monitores u otros canales de comunicación de la red para que las
                                    peticiones puedan conectar. </p>

                                <p>
                                    <center>
                                        <h4>SERVIDOR WEB </h4>
                                    </center>
                                </p>
                                <p>Este almacena documentos HTML, imágenes, archivos de texto, escrituras y demas material web. Ademas de incluir contenido
                                    que se distribuye en clientes que lo solicitan en la red. </p>

                                <p>
                                    <center>
                                        <h4>SERVIDOR DE BASE DE DATOS </h4>
                                    </center>
                                </p>
                                <p>Este provee servicio de base de datos a otros programas y otras computadoras</p>

                                <p>
                                    <center>
                                        <h4>SERVIDOR DE IMPRESORAS </h4>
                                    </center>
                                </p>
                                <p>Controla una o mas impresoras, y pone en cola los trabajos de impresión de clientes que esten en la red. </p>

                                <p>
                                    <center>
                                        <h4>SERVIDOR DE FAX </h4>
                                    </center>
                                </p>
                                <p>Este almacena, envia, recibe, enruta y realiza otras funciones necesarias para la transmicion, recepcion y distribucion
                                    apropiadas para los fax. </p>

                                <p>
                                    <center>
                                        <h4>SERVIDOR DE SEGURIDAD </h4>
                                    </center>
                                </p>
                                <p>Este tiene un software especializado para detener intrusiones maliciosas. </p>

