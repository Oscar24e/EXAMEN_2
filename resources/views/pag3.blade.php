                            <h2 class="entry-title">
                                <a title="ventajas y desventajas de la arquitectura  Cliente/Servidor ">
                                    Ventajas y desventajas de la arquitectura Cliente/Servidor </a>
                            </h2>
                            <div class="post-content">
                                <p>
                                    <center>
                                        <h2>VENTAJAS</h2>
                                    </center>
                                </p>

                                <p>
                                    <center>
                                        <h4>CENTRALIZACION DEL CONTROL </h4>
                                    </center>
                                </p>
                                <P>Los accesos, recursos y la integridad de los datos son controlados por el servidor de forma que un programa cliente defectuoso o no autorizado no pueda dañar el sistema.</P>
                                <p>
                                    <center>
                                        <h4>ESCALABILIDAD </h4>
                                    </center>
                                </p>
                                <P>Se puede aumentar la capacidad de clientes y servidores por separado</P>
                                <p>
                                    <center>
                                        <h4>FACIL MANTENIMIENTO </h4>
                                    </center>
                                </p>
                                <P>Es posible reemplazar, reparar, actualizar, o incluso trasladar un servidor, mientras que sus clientes no se verán afectados por ese cambio</P>
                                <p>
                                    <center>
                                        <h4>TECNOLOGIAS CAPACES </h4>
                                    </center>
                                </p>
                                <P>Existen tecnologías suficientemente desarrolladas que aseguran la seguridad en las transacciones</P>
                                <p>
                                    <center>
                                        <h4>SEGURIDAD</h4>
                                    </center>
                                </p>
                                <P>Los clientes no tienen acceso a las IP's por lo que se dificulta el rastreo y/o hackeo de los usuarios</P>

                                <p>
                                    <center>
                                        <h2>DESVENTAJAS</h2>
                                    </center>
                                </p>

                                <p>
                                    <center>
                                        <h4>CONGESTION DE TRAFICO </h4>
                                    </center>
                                </p>
                                <P>Cuando una gran cantidad de clientes envían peticiones </P>
                                <p>
                                    <center>
                                        <h4>NO HAY ROBUSTEZ </h4>
                                    </center>
                                </p>
                                <P>No tiene la robustez de una red P2P. Cuando un servidor está caído, las peticiones de los clientes no pueden ser satisfechas</P>
                                <p>
                                    <center>
                                        <h4>HAY LIMITES </h4>
                                    </center>
                                </p>
                                <P>El software y el hardware de un servidor son generalmente muy determinantes. Un hardware regular de un ordenador personal puede no poder servir a cierta cantidad de clientes. Normalmente se necesita software y hardware específico, sobre todo en el lado del servidor, para satisfacer el trabajo</P>
                                <p>
                                    <center>
                                        <h4>DISPONIBILIDAD DE RECURSOS EN EL SERVIDOR </h4>
                                    </center>
                                </p>
                                <P>El cliente no dispone de los recursos que puedan existir en el servidor. Por ejemplo, si es una aplicación web, no podremos escribir en el disco duro del cliente o imprimir directamente sobre las impresoras sin sacar antes la ventana previa de impresión de los navegadores.</P>
                                <p>
                                    <center>
                                        <h4>UNICA VIA DE ACCESO ES EL SERVIDOR </h4>
                                    </center>
                                </p>
                                <P>La única forma de obtener la información es a través de la que proporciona el servidor, con lo cual los clientes no podrán compartir información entre ellos</P>


